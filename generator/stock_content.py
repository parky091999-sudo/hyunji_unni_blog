def _etf_struct_fn(etf_content_type: str):
    if etf_content_type in _ETF_MARKET_LABEL:
        market_label = _ETF_MARKET_LABEL[etf_content_type]
        return lambda cfg: _struct_etf_individual(cfg, market_label)
    if etf_content_type == "sector_compare_kr":
        return lambda cfg: _struct_etf_sector_compare(cfg, "국내")
    if etf_content_type == "sector_compare_us":
        return lambda cfg: _struct_etf_sector_compare(cfg, "미국")
    if etf_content_type == "tax_account":
        return _struct_etf_tax_account
    # 폴백(콘텐츠 타입 미상) — 미국 섹터비교 형식
    return lambda cfg: _struct_etf_sector_compare(cfg, "미국")


def _build_stock_system(topic_id: str, cfg: dict, etf_content_type: str | None = None) -> str:
    if topic_id == "etf포트폴리오":
        etf_content_type = etf_content_type or "sector_compare_us"
        struct_fn = _etf_struct_fn(etf_content_type)
        checklist = _ETF_CHECKLIST.get(etf_content_type, "- [소제목] 6개\n")
        multi_image = etf_content_type != "tax_account"
    else:
        struct_fn = _STRUCT_BUILDERS.get(topic_id, _struct_ipo)
        checklist = _CHECKLIST.get(topic_id, "- [소제목] 6개\n")
        multi_image = topic_id in _MULTI_IMAGE_TOPICS

    photo_rule = (
        "- [사진1], [사진2] 정확히 1개씩(각 위치는 구조대로). [사진3]+ 금지\n"
        if multi_image
        else "- [사진1] 1개만. [사진2]+ 금지\n"
    )
    return (
        _COMMON_RULES
        + f"\n[기준 시점] {_today_str()} — 제목·서두에 명시\n"
        + struct_fn(cfg)
        + _OUTPUT_FORMAT
        + "\n[마커 체크리스트 — 누락 시 재작성]\n"
        + photo_rule
        + "- [요약시작]~[요약끝] 1쌍\n"
        "- [표시작]~[표끝] 1쌍 (팩트만)\n"
        "- [FAQ시작]~[FAQ끝] 1쌍\n"
        + checklist
        + "- 구체 수치는 팩트 데이터 값만, 제도·원리 설명은 교육으로 풍부하게\n"
        "- 마무리: 면책·공식 확인 권장만 (공감/리뷰 CTA 금지)\n"
    )


def generate_stock_post(topic_id: str, fact_data: dict | list, api_key: str) -> dict | None:
    """팩트 데이터 기반 주식 인사이트 포스트 생성."""
    cfg = STOCK_TOPICS.get(topic_id)
    if not cfg:
        logger.error(f"알 수 없는 주식 소분류: {topic_id}")
        return None

    etf_content_type = fact_data.get("_etf_content_type") if isinstance(fact_data, dict) else None
    system = _build_stock_system(topic_id, cfg, etf_content_type)
    # "_"로 시작하는 내부용 필드(예: 차트 생성용 종목코드·콘텐츠타입·datetime)는 LLM 프롬프트에서 제외.
    # 중첩 dict(예: ETF 비교 데이터의 개별 종목 상세) 안에도 내부 필드가 있을 수 있어 재귀 처리.
    llm_fact_data = _strip_internal_fields(fact_data)
    facts_json = json.dumps(llm_fact_data, ensure_ascii=False, indent=2, default=str)
    category_hint = ""
    if topic_id == "종목분석":
        category_hint = (
            "네이버 카테고리 '주식분석' — 여러 종목을 나열하는 글이 아니라, "
            "오늘 고른 종목 '1개'만 깊게 파는 심층 분석 글. 팩트 데이터는 이미 선정된 단일 종목의 정보다.\n"
        )
    elif cfg.get("blog_category") == "주식분석":
        category_hint = "네이버 카테고리 '주식분석' — 데이터 정리 톤으로 작성.\n"

    user_msg = (
        f"소분류: {cfg['name']}\n"
        f"네이버 블로그 카테고리: {cfg.get('blog_category', '')}\n"
        f"기준일: {_today_str()}\n"
        f"{category_hint}\n"
        f"[팩트 데이터 — 이 데이터만 사용, 추가·변조 금지]\n{facts_json}\n\n"
        "위 팩트만으로 네이버 블로그 원고를 작성해라. "
        "표 행은 제공된 데이터 항목만 채워라. 데이터가 5건 이하면 전부 표에 넣어라."
    )

    waits = [15, 40, 90]
    for attempt in range(1, len(waits) + 2):
        try:
            raw = _gen_text(api_key, user_msg, system, 8192, 0.15)
            if not raw:
                logger.error(f"{cfg['name']} 빈 응답 (시도 {attempt})")
                continue
            parsed = _parse_response(raw)
            if not parsed:
                logger.warning(f"{cfg['name']} 파싱 실패 (시도 {attempt})")
                continue

            parsed["title"] = strip_title_emphasis_markers(parsed.get("title", ""))
            parsed["body"] = strip_body_emphasis_markers(parsed.get("body", ""))

            body_len = len(_IMAGE_MARKER.sub("", parsed.get("body", "")))
            if body_len < 700:
                logger.warning(f"{cfg['name']} 본문 짧음 ({body_len}자) — 재생성")
                continue

            qr = score_stock_content(
                title=parsed.get("title", ""),
                body=parsed.get("body", ""),
                tags=parsed.get("tags", []),
                table_str=parsed.get("table_str", ""),
                faq_str=parsed.get("faq_str", ""),
                topic_id=topic_id,
                fact_data=llm_fact_data,
                subheading_count=len(parsed.get("subheadings", [])),
            )
            parsed["quality_score"] = qr["score"]
            if not qr["pass"] or qr["needs_retry"]:
                logger.warning(
                    f"{cfg['name']} 품질 미달 ({qr['score']}점) — 재생성: "
                    f"{'; '.join(qr.get('critical', qr.get('issues', []))[:3])}"
                )
                continue

            body = parsed.get("body", "")
            body = re.sub(
                r"\n*이 리뷰가 마음에 들었으면 도움이 돼요 버튼을 눌러주세요\s*",
                "\n",
                body,
            )
            parsed["body"] = body.rstrip()

            if not parsed.get("table_strs"):
                logger.warning(f"{cfg['name']} 표 누락 — 재생성")
                continue

            if "[사진1]" not in body:
                # [사진1]이 없으면 헤더 카드(브랜드 이미지)가 삽입될 앵커가 없어 조용히 누락됨
                logger.warning(f"{cfg['name']} [사진1] 마커 누락 — 재생성")
                continue

            logger.info(
                f"{cfg['name']} 생성 완료: {parsed.get('title')!r} "
                f"(본문 {body_len}자, 표={bool(parsed.get('table_strs'))})"
            )
            parsed["stock_topic"] = topic_id
            parsed["fact_data"] = fact_data
            return parsed

        except Exception as e:
            logger.error(f"{cfg['name']} 생성 실패 (시도 {attempt}): {e}")
            if attempt <= len(waits):
                time.sleep(waits[attempt - 1])
    return None
