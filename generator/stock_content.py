_WEEKDAY_KO = ["월", "화", "수", "목", "금", "토", "일"]

def _today_str() -> str:
    return datetime.now(KST).strftime("%Y년 %m월 %d일")


def _is_weekend() -> bool:
    return datetime.now(KST).weekday() >= 5


def _base_time_line() -> str:
    """기준 시점 라인 — 주말이면 휴장 사실을 프롬프트에 명시해 '오늘 마감' 오표기를 막는다."""
    now = datetime.now(KST)
    wd = _WEEKDAY_KO[now.weekday()]
    line = f"[기준 시점] {_today_str()}({wd}) — 제목·서두에 명시"
    if _is_weekend():
        line += (
            "\n★오늘은 주말(국내·미국 증시 휴장)이다. 팩트 데이터의 시세는 마지막 거래일 종가다. "
            "'오늘 상승/전일 대비 상승하며 마감' 같은 당일 거래 표현은 팩트 오류 — "
            "반드시 '지난 거래일(M월 D일) 마감 기준'으로 서술하라."
        )
    return line


def _strip_internal_fields(obj):
    """"_"로 시작하는 내부용 키(차트 티커, datetime 등)를 모든 중첩 depth에서 제거."""
    if isinstance(obj, dict):
        # 키가 int인 dict(연도별 데이터 등)도 안전하게 처리
        return {k: _strip_internal_fields(v) for k, v in obj.items() if not (isinstance(k, str) and k.startswith("_"))}
    if isinstance(obj, list):
        return [_strip_internal_fields(v) for v in obj]
    return obj


