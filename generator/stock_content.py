def _build_stock_system(
    topic_id: str, cfg: dict, etf_content_type: str | None = None, fact_data: dict | None = None
) -> str:
    fact = fact_data if isinstance(fact_data, dict) else {}
    # 심층 팩트 유무에 따라 구조(섹션·이미지 슬롯)가 달라진다 — 차트는 stock_post가 같은 조건으로 생성
    has_dividend = bool(fact.get("연도별배당(주당USD)"))
    has_financials = any(k.startswith("연간실적") for k in fact)
    has_naver_chart = fact.get("시장") == "국내" and bool(fact.get("_code"))
    etf_type = fact.get("_etf_type", "dividend")

    n_imgs = 1
    if topic_id == "etf포트폴리오":
        etf_content_type = etf_content_type or "sector_compare_us"
        # 국내 상장 ETF 개별분석은 네이버금융 공식 차트를 [사진3]으로 추가 —
        # 배당 차트([사진3][사진4])와 슬롯이 겹치므로 has_dividend와 배타(현재 kr은 배당팩트 없음)
        etf_has_naver = (
            etf_content_type in ("kr_individual", "kr_overseas_individual")
            and bool(fact.get("_etf_subject")) and not has_dividend
        )
        struct_fn = _etf_struct_fn(etf_content_type, has_dividend, etf_type, etf_has_naver)
        is_us_individual = etf_content_type == "us_individual"
        if has_dividend and etf_content_type in _ETF_MARKET_LABEL and (not is_us_individual or etf_type == "dividend"):
            # 배당 차트 2장([사진3][사진4])은 배당형에만
            checklist = _ETF_CHECKLIST_DIVIDEND
            n_imgs = 4
        elif etf_content_type in ("sector_compare_kr", "sector_compare_us"):
            checklist = _ETF_CHECKLIST.get(etf_content_type, "- [소제목] 6개\n")
            n_imgs = 3  # 헤더 + 비교 인포그래픽([사진2]) + 비교 차트([사진3])
        else:
            checklist = _ETF_CHECKLIST.get(etf_content_type, "- [소제목] 6개\n")
            n_imgs = 2 if etf_content_type != "tax_account" else 1
            if etf_has_naver:
                n_imgs = 3
    elif topic_id == "종목분석":
        if _is_weekend():
            # 주말엔 당일 시황이 없으므로 주간 리뷰+월요일 관전포인트 구조로 전환
            struct_fn = lambda c: _struct_single_stock_weekend(c, has_financials, has_naver_chart)
            checklist = _CHECKLIST_WEEKEND_STOCK
        else:
            struct_fn = lambda c: _struct_single_stock(c, has_financials, has_naver_chart)
            checklist = _CHECKLIST["종목분석"]
        n_imgs = 2 + (1 if has_naver_chart else 0) + (1 if has_financials else 0)
    elif topic_id == "공모주캘린더" and fact.get("_ipo_mode") == "deep":
        # 개별 공모주 심층분석(청약 임박 종목 1개) — 균등/비례/패스 판단 가이드
        struct_fn = _struct_ipo_deep
        checklist = _CHECKLIST_IPO_DEEP
        n_imgs = 1
    else:
        struct_fn = _STRUCT_BUILDERS.get(topic_id, _struct_ipo)
        checklist = _CHECKLIST.get(topic_id, "- [소제목] 6개\n")
        n_imgs = 2 if topic_id in _MULTI_IMAGE_TOPICS else 1

    if n_imgs == 1:
        photo_rule = "- [사진1] 1개만. [사진2]+ 금지\n"
    else:
        slots = ", ".join(f"[사진{i}]" for i in range(1, n_imgs + 1))
        photo_rule = f"- {slots} 정확히 1개씩(각 위치는 구조대로). [사진{n_imgs + 1}]+ 금지\n"
    return (
        _COMMON_RULES
        + "\n" + _base_time_line() + "\n"
        + struct_fn(cfg)
        + _OUTPUT_FORMAT
        + "\n[마커 체크리스트 — 누락 시 재작성]\n"
        + photo_rule
        + "- [요약시작]~[요약끝] 1쌍\n"
        "- [표시작]~[표끝] 1쌍 (팩트만)\n"
        "- [FAQ시작]~[FAQ끝] 1쌍\n"
        + checklist
        + "- 구체 수치는 팩트 데이터 값만, 제도·원리 설명은 교육으로 풍부하게\n"
        "- 마무리: 면책·공식 확인 권장만 (공감/리뷰 CTA 금지)\n"
    )


