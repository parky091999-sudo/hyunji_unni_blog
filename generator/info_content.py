_INVEST_KW_RE = re.compile(r"(주식|배당|ETF|etf|펀드|투자|채권|리츠|공모주|코인|비트코인)")
_ACTION_KW_RE = re.compile(r"(해지|환급금|청구|수령|인출|갈아타기|전환|리모델링|정산)")


def _adapt_cfg_to_topic(cfg: dict, keyword: str) -> dict:
    """키워드 유형(투자·개념형/행위형)에 맞게 소제목·요약 라벨을 교체한 cfg 사본을 반환."""
    c = dict(cfg)
    if _INVEST_KW_RE.search(keyword):
        c["sec2"] = "시작 조건 — 뭐가 필요하나?"
        c["sec4"] = "시작 방법 — 어디서 어떻게"
        c["summary"] = ["✔ 핵심 수익·조건: (한 줄)", "✔ 시작 조건: (계좌·금액)", "✔ 시작 방법: (경로 한 줄)"]
        logger.info(f"소제목 세트: 투자·개념형 적용 ({keyword!r})")
    elif _ACTION_KW_RE.search(keyword):
        c["sec2"] = "대상 확인 — 내 경우는?"
        c["sec4"] = "처리 방법 — 어디서 어떻게"
        c["summary"] = ["✔ 핵심 내용: (한 줄)", "✔ 대상·조건: (한 줄)", "✔ 확인 방법: (경로 한 줄)"]
        logger.info(f"소제목 세트: 행위형 적용 ({keyword!r})")
    return c


def generate_info_post(keyword: str, api_key: str, info_cat_id: str) -> dict | None:
    """고CPC 정보성 포스트 생성 (금융재테크·세금절세·보험·부동산주거).
    팩트 수집(Naver 뉴스·공공API) → Gemini Search Grounding → 글 생성."""
    cfg = INFO_CATEGORIES.get(info_cat_id)
    if not cfg:
        logger.error(f"알 수 없는 정보 카테고리: {info_cat_id}")
        return None
    cfg = _adapt_cfg_to_topic(cfg, keyword)

    # 팩트 블록 수집 (Naver 뉴스 + 공공데이터포털)
    try:
        from generator.info_collector import collect_info_facts
        fact_block = collect_info_facts(info_cat_id, keyword)
    except Exception as e:
        logger.warning(f"팩트 수집 스킵: {e}")
        fact_block = ""

    # 검증 수치 주입(2026-07-19, gov FORCE_FACTS와 동일 패턴): 오류 글 재발행 시 관리자가
    # 공식 확인한 고시값·배점표를 최우선 팩트로 — 모델의 학습 기억 창작 차단.
    import os as _os
    forced_facts = _os.environ.get("FORCE_FACTS", "").strip()
    if forced_facts:
        logger.info(f"검증 수치 주입(FORCE_FACTS {len(forced_facts)}자)")
        fact_block = ("[검증된 공식 수치 — 금액·배점·세율은 반드시 아래 값만 문자 그대로 사용, "
                      "그 외 수치 창작 절대 금지]\n" + forced_facts + "\n\n") + fact_block

    # 법정 고시값 상시 주입(2026-07-19, 오류 원천 차단) — 관련 키워드면 검증 DB 값이 항상 최상단
    try:
        from generator.official_facts import lookup_block
        official = lookup_block(keyword, cfg["name"] + " " + info_cat_id)
        if official:
            fact_block = official + fact_block
    except Exception as e:
        logger.warning(f"고시값 DB 스킵: {e}")

    system = _build_info_system(cfg)
    base_user_msg = (
        f"'{cfg['name']}' 키워드: {keyword}"
        + f"\n\n위 키워드로 모바일 독자가 5분 안에 핵심을 파악할 수 있는 {cfg['name']} 정리 글을 작성해라."
        + "\n- 표 정확히 1개(한눈에 정리, 3열): [표시작]...[표끝] 마커 1쌍, 각 셀 10자 이내, 빈칸 금지."
        + "\n- FAQ 정확히 1개: [FAQ시작]...[FAQ끝] 마커 1쌍."
        + "\n- ★소제목 6개, 각 줄 반드시 '[소제목] '으로 시작(마커 필수). [요약시작]...[요약끝] 1쌍([사진1] 뒤)."
        + "\n- ★불릿('· ')은 '자격/대상'·'꼭 알아둘 점' 두 섹션에서만 각 3~4줄. 나머지는 일반 문장(불릿 도배 금지)."
        + "\n- ★본문 2,200자 이상 심화(요약·표·FAQ 블록을 뺀 본문 문단 기준 — 계산 예시 2~3개·출처 1줄·FAQ 답 2~4줄). 소제목엔 번호 금지."
        + "\n- ★표 아래 '출처: (공식 기관)' 1줄 필수. 팩트·뉴스에 없는 수치는 쓰지 마라."
        + "\n- 이미지는 [사진1](헤더) 1개만. 본문 스톡사진([사진2]+) 금지 — 정보만 전달."
        + "\n- 제목에 구체적 수치·금액 포함."
        + "\n- ★2026년 최신 기준으로 작성. '확인하세요'/'상담하세요' 문구 금지."
    )
    user_msg = fact_block + base_user_msg

    waits = [15, 40, 90]
    best_short = None   # (body_len, parsed) — 길이 외 게이트 전부 통과한 최선 후보(폴백용)
    feedback = ""       # 재생성 교정 피드백(직전 실패 사유 반영)
    for attempt in range(1, len(waits) + 2):
        try:
            raw = _gen_text(api_key, user_msg + feedback, system, 8192, 0.8, use_search=True)
            if not raw:
                logger.error(f"{cfg['name']}글 빈 응답 (시도 {attempt})")
                continue
            parsed = _parse_response(raw)
            if not parsed:
                logger.warning(f"{cfg['name']}글 파싱 실패 (시도 {attempt})")
                continue
            parsed["title"] = strip_title_emphasis_markers(parsed.get("title", ""))
            date_critical = validate_info_dates(parsed.get("title", ""), parsed.get("body", ""))
            if date_critical:
                feedback = f"\n\n⚠️직전 초안 날짜 오류({date_critical[0]}). 2026년 기준 정확한 날짜만 써라."
                logger.warning(
                    f"{cfg['name']}글 날짜 오류 — 재생성: {'; '.join(date_critical[:2])}"
                )
                continue
            body = parsed.get("body", "")
            body_len = len(_IMAGE_MARKER.sub("", body))
            # ── 길이 외 품질 게이트 먼저(길이만 부족한 최선 후보를 살리기 위해 길이 검사는 맨 뒤) ──
            calc_src = body + "\n" + "\n".join(parsed.get("table_strs", []) or [])
            if not _CALC_SIGNAL_RE.search(calc_src):
                feedback = "\n\n⚠️직전 초안에 계산 예시가 없었다. '월 X원 → 연 Y원' 식 구체 계산 예시를 2~3개 넣어라."
                logger.warning(f"{cfg['name']}글 계산 예시 없음 — 재생성")
                continue
            if "출처" not in body and "출처" not in calc_src:
                feedback = "\n\n⚠️직전 초안에 출처 표기가 없었다. 표 아래 '출처: (공식 기관)' 1줄을 반드시 넣어라."
                logger.warning(f"{cfg['name']}글 출처 표기 없음 — 재생성")
                continue
            # 구조 검증: 과불릿 도배(>25)·FAQ 누락·소제목 부족 시 재생성 (Gemini 변동성 대응)
            bullet_cnt = len(re.findall(r"(?m)^·\s", parsed.get("body", "")))
            faq_ok = bool(parsed.get("faq_pairs"))
            sub_cnt = len(parsed.get("subheadings", []))
            if bullet_cnt > 25 or not faq_ok or sub_cnt < 5:
                feedback = f"\n\n⚠️직전 초안 구조 불량(불릿 {bullet_cnt}/FAQ {faq_ok}/소제목 {sub_cnt}). 소제목 6개·FAQ 1개·불릿은 지정 2섹션만."
                logger.warning(f"{cfg['name']}글 구조 불량(불릿 {bullet_cnt}/FAQ {faq_ok}/소제목 {sub_cnt}) — 재생성")
                continue
            # 무근거 수치/카더라 하드 게이트 — 프롬프트 금지는 소프트라 어기면 그대로 발행됨
            # (보험 라이브 "평균 15% 저렴한 것으로 알려져 있어요" 실사고, 2026-07-06)
            unsourced = _UNSOURCED_RE.findall(parsed.get("body", ""))
            if unsourced:
                feedback = f"\n\n⚠️직전 초안에 무근거 표현({unsourced[0]!r})이 있었다. '알려져 있/전해지/카더라'류 금지 — 팩트에 있는 수치만."
                logger.warning(f"{cfg['name']}글 무근거 인용 표현 {len(unsourced)}건({unsourced[0]!r} 등) — 재생성")
                continue
            # ── 길이 검사(맨 뒤): 부족하면 최선 후보로 보관하고 길이 피드백 후 재생성 ──
            if body_len < INFO_BODY_MIN:
                if best_short is None or body_len > best_short[0]:
                    best_short = (body_len, parsed)  # 길이 외 전부 통과한 후보만 보관
                # 백지 재생성은 같은 길이대에 반복 수렴함(2026-07-13 실측: 1670→1731→1737자
                # 4연속 미달로 발행 누락) → 직전 초안을 되먹여 '확장 재작성'시키는 게 확실히 늘어남.
                feedback = (f"\n\n⚠️아래 [직전 초안]은 본문 문단(요약·표·FAQ 블록 제외)이 {body_len}자로 "
                            f"목표 {INFO_BODY_MIN}자에 미달했다. 초안의 구조·마커·사실은 그대로 유지하면서 "
                            "각 소제목 단락에 계산 예시·근거 수치·비교·주의점을 2~3문장씩 덧붙여 "
                            "전체 글을 처음부터 다시 출력하라(요약·표·FAQ를 늘리는 건 글자수에 안 잡힌다).\n\n"
                            "[직전 초안]\n" + raw)
                logger.warning(f"{cfg['name']}글 본문 짧음 ({body_len}자, 목표 {INFO_BODY_MIN}자) — 재생성(직전 초안 확장)")
                continue
            logger.info(
                f"{cfg['name']}글 생성 완료: {parsed.get('title')!r} "
                f"(본문 {body_len}자, 표={bool(parsed.get('table_strs'))}, "
                f"FAQ={bool(parsed.get('faq_pairs'))}, 요약={bool(parsed.get('summary_text'))})"
            )
            try:
                refined_raw = _gen_text(api_key, f"아래 초안을 퇴고해줘:\n\n{raw}", _INFO_REFINE_SYSTEM, 8192, 0.75)
                if refined_raw:
                    refined = _parse_response(refined_raw)
                    ref_bullets = len(re.findall(r"(?m)^·\s", refined.get("body", ""))) if refined else 999
                    # 퇴고가 구조(소제목·표·FAQ) 보존 + 과불릿(>25) 아닐 때만 채택 — 아니면 원본 사용
                    if (refined
                            and len(_IMAGE_MARKER.sub("", refined.get("body", ""))) >= INFO_BODY_MIN
                            and len(refined.get("subheadings", [])) >= len(parsed.get("subheadings", []))
                            and bool(refined.get("table_strs")) >= bool(parsed.get("table_strs"))
                            and bool(refined.get("faq_pairs")) >= bool(parsed.get("faq_pairs"))
                            and ref_bullets <= 25
                            and not _UNSOURCED_RE.search(refined.get("body", ""))):
                        return refined
                    logger.info(f"{cfg['name']}글 퇴고 거부(소제목 {len(refined.get('subheadings', [])) if refined else 0}, 불릿 {ref_bullets}, FAQ {bool(refined.get('faq_pairs')) if refined else False}) — 원본 사용")
            except Exception as e:
                logger.warning(f"{cfg['name']}글 퇴고 실패(원본 사용): {e}")
            return parsed
        except Exception as e:
            logger.error(f"{cfg['name']}글 생성 실패 (시도 {attempt}/{len(waits)+1}): {e}")
            if attempt <= len(waits):
                time.sleep(waits[attempt - 1])
    # 재시도 소진 — 길이만 부족했던 최선 초안이 하한(1800) 이상이면 '발행 누락' 대신 그것으로 발행.
    # 품질게이트(계산·출처·구조·무근거)는 이미 전부 통과한 후보라 발행 안전. 목표 2000엔 못 미쳐도
    # 최적범위(1000~2000, §6) 안이면 빈 발행보다 낫다(2026-07-12 보험·세금 매일 실패 대응).
    if best_short and best_short[0] >= INFO_BODY_FLOOR:
        logger.warning(
            f"{cfg['name']}글 목표({INFO_BODY_MIN}) 미달이나 품질게이트 통과 {best_short[0]}자 "
            f"— 폴백 발행(누락 방지)"
        )
        return best_short[1]
    return None
