def generate_info_post(keyword: str, api_key: str, info_cat_id: str) -> dict | None:
    """고CPC 정보성 포스트 생성 (금융재테크·세금절세·보험·부동산주거)."""
    cfg = INFO_CATEGORIES.get(info_cat_id)
    if not cfg:
        logger.error(f"알 수 없는 정보 카테고리: {info_cat_id}")
        return None
    system = _build_info_system(cfg)
    user_msg = (
        f"'{cfg['name']}' 키워드: {keyword}"
        + f"\n\n위 키워드로 모바일 독자가 5분 안에 핵심을 파악할 수 있는 {cfg['name']} 정리 글을 작성해라."
        + "\n- 표 정확히 1개(한눈에 정리, 3열): [표시작]...[표끝] 마커 1쌍, 각 셀 10자 이내, 빈칸 금지."
        + "\n- FAQ 정확히 1개: [FAQ시작]...[FAQ끝] 마커 1쌍."
        + "\n- ★소제목 5개, 각 줄 반드시 '[소제목] '으로 시작(마커 필수). [요약시작]...[요약끝] 1쌍([사진1] 뒤)."
        + "\n- ★불릿('· ')은 '자격/대상'·'꼭 알아둘 점' 두 섹션에서만 각 3~4줄. 나머지는 일반 문장(불릿 도배 금지)."
        + "\n- ★본문 1,800자 이상 충분히 상세히(각 소제목 아래 설명 2~3문장, 자격 5~6줄, FAQ 답변 2~3줄). 소제목엔 번호 금지."
        + "\n- 이미지는 [사진1](헤더) 1개만. 본문 스톡사진([사진2]+) 금지 — 정보만 전달."
        + "\n- 제목에 구체적 수치·금액 포함."
    )

    waits = [15, 40, 90]
    for attempt in range(1, len(waits) + 2):
        try:
            raw = _gen_text(api_key, user_msg, system, 8192, 0.8)
            if not raw:
                logger.error(f"{cfg['name']}글 빈 응답 (시도 {attempt})")
                continue
            parsed = _parse_response(raw)
            if not parsed:
                logger.warning(f"{cfg['name']}글 파싱 실패 (시도 {attempt})")
                continue
            body_len = len(_IMAGE_MARKER.sub("", parsed.get("body", "")))
            # 표/FAQ/요약 추출 후 prose 기준. 1,800자 목표 글이면 prose ~1,000자+ 정상.
            if body_len < 1000:
                logger.warning(f"{cfg['name']}글 본문 짧음 ({body_len}자, prose 1000자+ 목표) — 재생성")
                continue
            logger.info(
                f"{cfg['name']}글 생성 완료: {parsed.get('title')!r} "
                f"(본문 {body_len}자, 표={bool(parsed.get('table_strs'))}, "
                f"FAQ={bool(parsed.get('faq_pairs'))}, 요약={bool(parsed.get('summary_text'))})"
            )
            try:
                refined_raw = _gen_text(api_key, f"아래 초안을 퇴고해줘:\n\n{raw}", _INFO_REFINE_SYSTEM, 8192, 0.75)
                if refined_raw:
                    refined = _parse_response(refined_raw)
                    # 퇴고가 구조(소제목·표)를 보존했을 때만 채택 — 마커 누락 시 원본 사용
                    if (refined
                            and len(_IMAGE_MARKER.sub("", refined.get("body", ""))) >= 800
                            and len(refined.get("subheadings", [])) >= len(parsed.get("subheadings", []))
                            and bool(refined.get("table_strs")) >= bool(parsed.get("table_strs"))):
                        return refined
                    logger.info(f"{cfg['name']}글 퇴고가 구조 훼손(소제목 {len(refined.get('subheadings', [])) if refined else 0}<{len(parsed.get('subheadings', []))}) — 원본 사용")
            except Exception as e:
                logger.warning(f"{cfg['name']}글 퇴고 실패(원본 사용): {e}")
            return parsed
        except Exception as e:
            logger.error(f"{cfg['name']}글 생성 실패 (시도 {attempt}/{len(waits)+1}): {e}")
            if attempt <= len(waits):
                time.sleep(waits[attempt - 1])
    return None
